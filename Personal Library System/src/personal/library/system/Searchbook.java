    public void showtable(String Name){
        try{
            Connection c = Sqlregfrnd.getConnection();
            PreparedStatement stmt = c.prepareStatement("select * from book_data where Book_name like ?");
            String n = new String();
            n = "%" + Name + "%";
            stmt.setString(1, n);
            ResultSet rs = stmt.executeQuery();
            if(!rs.next()){
                
            }
            else{
                
                DefaultTableModel o = (DefaultTableModel) jTable1.getModel();
                while(o.getRowCount() > 0){
                    o.removeRow(0);
                }
                rs.beforeFirst();
                while(rs.next())
                {
                    String issue = new String();
                    String borrow = new String();
                    int is = rs.getInt("bookissued");
                    int bo = rs.getInt("bookborrowed");
                    if(is==1){
                        issue = "Book issued out";
                    }
                    else{
                        issue = "Not issued";
                    }
                    if(bo==1){
                        borrow= "Yes";
                    }
                    else{
                        borrow = "No";
                    }
                 o.addRow(new Object[]{rs.getString("Book_name"),rs.getString("Publisher"),rs.getInt("serialno"),issue,borrow});
                }
            }
        }catch(Exception e){
            System.out.println(e);
        }
    }
